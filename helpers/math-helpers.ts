


export function addTwoNumbers( num1: number, num2: number ): number {
    return num1 + num2;
}

export function substractTwoNumbers( num1: number, num2: number ): number {
    return num1 - num2;
}

export const timesTwoNumbers = ( num1: number, num2: number ): number =>  {
    return num1 * num2;
}

export const divideTwoNumbers = ( num1: number, num2: number ): number =>  {
    return num1 / num2;
}
