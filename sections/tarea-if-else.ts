


// Realizar un programa que muestre la nota del alumno como
// A, B, C, D, F
// donde:

// A >= 90
// B >= 80
// C >= 70
// D >= 60
// F  < 60

let note:number = 59;

if ( note >= 90 ) {
    console.log('El alumno sacó una A');
} else if ( note >= 80 ) {
    console.log('El alumno sacó una B');
} else if ( note >= 70 ) {
    console.log('El alumno sacó una C');
} else if ( note >= 60 ) {
    console.log('El alumno sacó una D');
} else {
    console.log('El alumno sacó una F');
}






